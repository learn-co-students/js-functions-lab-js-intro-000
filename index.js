function happyHolidayTo(holidayName, recipient) {
  return `Happy ${holidayName}, ${recipient}!`;
}

function holidayCountdown(holidayName, daysToHoliday) {
  return `It\'s ${daysToHoliday} days until ${holidayName}!`;
}
