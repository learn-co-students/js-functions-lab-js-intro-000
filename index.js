function happyHolidays() {
  return "Happy holidays!";
}

function happyHolidaysTo(Aaron) {
  return `Happy holidays, ${Aaron}!`
}

function happyHolidayTo(birthday, Aaron) {
  return `Happy ${birthday}, ${Aaron}!`
}

function holidayCountdown(Christmas, days) {
  return `It's ${days} days until ${Christmas}!`
}
