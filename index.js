

function happyHolidays() {
  return "Happy holidays!"
}

function happyHolidaysTo(whom) {
  return 'Happy holidays, ' + whom + '!'
}

function happyHolidayTo(occasion, whom) {
  return `Happy ${occasion}, ${whom}!`
}

function holidayCountdown(occasion, sleeps) {
  return `It's ${sleeps} days until ${occasion}!`
}
