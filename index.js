

function happyHolidays() {
  return "Happy holidays!"
}

function happyHolidaysTo(Raeven) {
  return `Happy holidays, ${Raeven}!`
}

function happyCustomHolidayTo(holiday, Raeven) {
  return `Happy ${holiday}, ${Raeven}!`
}

function holidayCountdown(holiday, days) {
  return `It\'s ${days} days until ${holiday}!`
}
