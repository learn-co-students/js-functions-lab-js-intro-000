function happyHolidays() {
  return "Happy holidays!";
}

function happyHolidaysTo (Karin) {
  return `Happy holidays, ${Karin}!`;
}

function happyHolidayTo(holiday, Karin) {
  return `Happy ${holiday}, ${Karin}!`;
}

function holidayCountdown(holiday, days) {
  return `It's ${days} days until ${holiday}!`;
}
